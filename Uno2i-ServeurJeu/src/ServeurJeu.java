    private void demarrerBoutonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_demarrerBoutonActionPerformed
        if(!nomServeur.getText().isEmpty()) {
            nomServeur.setEnabled(false);
            
            serveur = new Serveur(nomServeur.getText());
            serveur.start();

            demarrerBouton.setEnabled(false);
            demarrerBouton.setText("Démarré");

            stopperBouton.setText("Stopper");
            stopperBouton.setEnabled(true);
            
            this.setTitle("DÉMARRÉ - Serveur de jeu \"" + nomServeur.getText() + "\"");
        }
        else {
            JOptionPane.showMessageDialog(null, "Veuillez saisir un nom pour le serveur de jeu.");
        }
    }//GEN-LAST:event_demarrerBoutonActionPerformed

    private void stopperBoutonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stopperBoutonActionPerformed
        serveur.stopper();
        
        stopperBouton.setEnabled(false);
        stopperBouton.setText("Stoppé");
        
        demarrerBouton.setText("Démarrer");
        demarrerBouton.setEnabled(true);
        
        nomServeur.setEnabled(true);
        
        this.setTitle("STOPPÉ - Serveur de jeu \"" + nomServeur.getText() + "\"");
    }//GEN-LAST:event_stopperBoutonActionPerformed

